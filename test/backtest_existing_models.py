def require_file(path: Path) -> None:
    if not path.exists():
        raise FileNotFoundError(f"Required input file is missing: {path}")


def read_existing_returns(input_dir: Path) -> pd.DataFrame:
    spo_path = input_dir / "spo_backtest_returns.csv"
    ai_path = input_dir / "ai_risk_profile_backtest_returns.csv"
    require_file(spo_path)
    require_file(ai_path)

    frames = [
        pd.read_csv(spo_path, parse_dates=["date"]),
        pd.read_csv(ai_path, parse_dates=["date"]),
    ]
    returns = pd.concat(frames, ignore_index=True)
    returns = returns.drop_duplicates(["date", "strategy"], keep="last")
    expected_cols = {
        "date",
        "strategy",
        "gross_return",
        "turnover",
        "transaction_cost_rate",
        "transaction_cost",
        "net_return",
    }
    missing = expected_cols.difference(returns.columns)
    if missing:
        raise ValueError(f"Backtest return table is missing columns: {sorted(missing)}")
    return returns.sort_values(["strategy", "date"]).reset_index(drop=True)


def apply_transaction_cost(backtest_returns: pd.DataFrame, cost_rate: float) -> pd.DataFrame:
    out = backtest_returns.copy()
    out["source_net_return"] = out["net_return"]
    out["source_transaction_cost_rate"] = out["transaction_cost_rate"]
    out["transaction_cost_rate"] = float(cost_rate)
    out["transaction_cost"] = out["turnover"].astype(float) * float(cost_rate)
    out["net_return"] = out["gross_return"].astype(float) - out["transaction_cost"]
    return out


