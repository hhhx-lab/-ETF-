def save_ai_weight_heatmap(weights: pd.DataFrame, output_dir: Path) -> str:
    plot_dir = ensure_plot_dir(output_dir)
    path = plot_dir / "ai_monthly_weight_heatmap.png"
    ai = weights[weights["strategy"].str.startswith("ai_")].copy()
    if ai.empty:
        raise ValueError("No AI strategy weights found for heatmap.")
    ai["date"] = pd.to_datetime(ai["date"]).dt.strftime("%Y-%m")
    matrix = ai.pivot_table(
        index=["strategy", "date"],
        columns="ticker",
        values="weight",
        aggfunc="sum",
    ).reindex(columns=TICKERS)
    fig_height = max(6, 0.18 * len(matrix))
    fig, ax = plt.subplots(figsize=(9, fig_height))
    image = ax.imshow(matrix.to_numpy(dtype=float), aspect="auto", cmap="YlGnBu", vmin=0, vmax=1)
    ax.set_title("AI Monthly Portfolio Weights")
    ax.set_xticks(range(len(matrix.columns)))
    ax.set_xticklabels(matrix.columns)
    ax.set_yticks(range(len(matrix.index)))
    ax.set_yticklabels([f"{s} {d}" for s, d in matrix.index], fontsize=6)
    fig.colorbar(image, ax=ax, label="Weight")
    fig.tight_layout()
    fig.savefig(path, dpi=160)
    plt.close(fig)
    return str(path)


