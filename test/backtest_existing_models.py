def max_drawdown(return_series: pd.Series) -> float:
    wealth = (1.0 + return_series.fillna(0.0)).cumprod()
    if wealth.empty:
        return 0.0
    return float((wealth / wealth.cummax() - 1.0).min())


def drawdown_series_from_wealth(wealth: pd.Series) -> pd.Series:
    if wealth.empty:
        return wealth
    return wealth / wealth.cummax() - 1.0


def longest_drawdown_months(return_series: pd.Series) -> int:
    wealth = (1.0 + return_series.fillna(0.0)).cumprod()
    if wealth.empty:
        return 0
    peak = wealth.cummax()
    below_peak = wealth < peak
    longest = 0
    current = 0
    for flag in below_peak:
        current = current + 1 if flag else 0
        longest = max(longest, current)
    return int(longest)


def evaluate_returns(returns: pd.Series, turnover: pd.Series) -> dict[str, Any]:
    clean_returns = returns.fillna(0.0).astype(float)
    n = len(clean_returns)
    wealth = float((1.0 + clean_returns).prod()) if n else 1.0
    annual_return = wealth ** (MONTHS_PER_YEAR / n) - 1.0 if n else 0.0
    annual_vol = (
        float(clean_returns.std(ddof=1) * np.sqrt(MONTHS_PER_YEAR)) if n > 1 else 0.0
    )
    downside = clean_returns[clean_returns < 0]
    downside_vol = (
        float(downside.std(ddof=1) * np.sqrt(MONTHS_PER_YEAR)) if len(downside) > 1 else 0.0
    )
    best_month = float(clean_returns.max()) if n else 0.0
    worst_month = float(clean_returns.min()) if n else 0.0
    mdd = max_drawdown(clean_returns)
    return {
        "months": int(n),
        "total_return": wealth - 1.0,
        "annual_return": annual_return,
        "annual_volatility": annual_vol,
        "sharpe": annual_return / annual_vol if annual_vol > 0 else 0.0,
        "sortino": annual_return / downside_vol if downside_vol > 0 else 0.0,
        "max_drawdown": mdd,
        "calmar": annual_return / abs(mdd) if mdd < 0 else 0.0,
        "longest_drawdown_months": longest_drawdown_months(clean_returns),
        "win_rate": float((clean_returns > 0).mean()) if n else 0.0,
        "best_month": best_month,
        "worst_month": worst_month,
        "average_monthly_return": float(clean_returns.mean()) if n else 0.0,
        "average_turnover": float(turnover.mean()) if len(turnover) else 0.0,
        "final_wealth": wealth,
    }


def calculate_metrics(backtest_returns: pd.DataFrame, cost_rate: float) -> pd.DataFrame:
    rows = []
    for strategy, group in backtest_returns.groupby("strategy", sort=True):
        for return_type, column, tx_rate in [
            ("gross", "gross_return", 0.0),
            ("net", "net_return", cost_rate),
        ]:
            rows.append(
                {
                    "strategy": strategy,
                    "strategy_group": MODEL_GROUPS.get(strategy, BENCHMARKS.get(strategy, "other")),
                    "return_type": return_type,
                    "transaction_cost_rate": tx_rate,
                    "strategy_note": STRATEGY_NOTES.get(strategy, ""),
                    **evaluate_returns(group[column], group["turnover"]),
                }
            )
    return pd.DataFrame(rows).sort_values(["return_type", "strategy"]).reset_index(drop=True)


def build_equity_curves(backtest_returns: pd.DataFrame) -> pd.DataFrame:
    rows = []
    for strategy, group in backtest_returns.groupby("strategy", sort=True):
        ordered = group.sort_values("date")
        gross_wealth = (1.0 + ordered["gross_return"].fillna(0.0)).cumprod()
        net_wealth = (1.0 + ordered["net_return"].fillna(0.0)).cumprod()
        gross_drawdown = drawdown_series_from_wealth(gross_wealth)
        net_drawdown = drawdown_series_from_wealth(net_wealth)
        for row, gross, net, gross_dd, net_dd in zip(
            ordered.itertuples(index=False),
            gross_wealth,
            net_wealth,
            gross_drawdown,
            net_drawdown,
            strict=False,
        ):
            rows.append(
                {
                    "date": row.date,
                    "strategy": row.strategy,
                    "gross_wealth": float(gross),
                    "net_wealth": float(net),
                    "gross_drawdown": float(gross_dd),
                    "net_drawdown": float(net_dd),
                    "gross_return": float(row.gross_return),
                    "net_return": float(row.net_return),
                    "turnover": float(row.turnover),
                    "transaction_cost": float(row.transaction_cost),
                }
            )
    return pd.DataFrame(rows).sort_values(["strategy", "date"]).reset_index(drop=True)


def build_excess_returns(backtest_returns: pd.DataFrame, benchmark: str) -> pd.DataFrame:
    pivot = backtest_returns.pivot(index="date", columns="strategy", values="net_return").sort_index()
    if benchmark not in pivot.columns:
        raise ValueError(f"Benchmark {benchmark} is not available in return table.")

    rows = []
    benchmark_returns = pivot[benchmark]
    for strategy in pivot.columns:
        if strategy == benchmark:
            continue
        excess = (pivot[strategy] - benchmark_returns).dropna()
        tracking_error = (
            float(excess.std(ddof=1) * np.sqrt(MONTHS_PER_YEAR)) if len(excess) > 1 else 0.0
        )
        annual_excess = float(excess.mean() * MONTHS_PER_YEAR) if len(excess) else 0.0
        rows.append(
            {
                "strategy": strategy,
                "benchmark": benchmark,
                "months": int(len(excess)),
                "annual_excess_return": annual_excess,
                "tracking_error": tracking_error,
                "information_ratio": annual_excess / tracking_error if tracking_error > 0 else 0.0,
                "excess_win_rate": float((excess > 0).mean()) if len(excess) else 0.0,
                "cumulative_excess_return": float((1.0 + excess).prod() - 1.0) if len(excess) else 0.0,
            }
        )
    return pd.DataFrame(rows).sort_values("annual_excess_return", ascending=False)


def build_cost_sensitivity(backtest_returns: pd.DataFrame, cost_rates: list[float]) -> pd.DataFrame:
    rows = []
    for strategy, group in backtest_returns.groupby("strategy", sort=True):
        for cost_rate in cost_rates:
            simulated = group["gross_return"] - cost_rate * group["turnover"]
            rows.append(
                {
                    "strategy": strategy,
                    "cost_rate": cost_rate,
                    **evaluate_returns(simulated, group["turnover"]),
                }
            )
    return pd.DataFrame(rows).sort_values(["cost_rate", "strategy"]).reset_index(drop=True)


def normalize_score(series: pd.Series, higher_is_better: bool = True) -> pd.Series:
    values = series.astype(float)
    if not higher_is_better:
        values = -values
    spread = values.max() - values.min()
    if spread <= 1e-12:
        return pd.Series(0.5, index=series.index)
    return (values - values.min()) / spread


def rank_models(metrics: pd.DataFrame) -> pd.DataFrame:
    net = metrics[
        (metrics["return_type"] == "net") & (metrics["strategy_group"] != "benchmark")
    ].copy()
    net["quality_score"] = (
        0.25 * normalize_score(net["annual_return"])
        + 0.20 * normalize_score(net["sharpe"])
        + 0.20 * normalize_score(net["max_drawdown"], higher_is_better=True)
        + 0.15 * normalize_score(net["win_rate"])
        + 0.10 * normalize_score(net["average_turnover"], higher_is_better=False)
        + 0.10 * normalize_score(net["calmar"])
    )
    net["rank"] = net["quality_score"].rank(ascending=False, method="first").astype(int)
    columns = [
        "rank",
        "strategy",
        "strategy_group",
        "quality_score",
        "total_return",
        "annual_return",
        "annual_volatility",
        "sharpe",
        "calmar",
        "max_drawdown",
        "win_rate",
        "average_turnover",
    ]
    return net.sort_values("rank")[columns].reset_index(drop=True)


def strategy_assessment(row: pd.Series) -> tuple[str, str]:
    strategy = row["strategy"]
    strength_parts = []
    weakness_parts = []

    if row["annual_return"] >= 0.20:
        strength_parts.append("年化收益领先")
    elif row["annual_return"] >= 0.12:
        strength_parts.append("年化收益中等偏稳")
    else:
        weakness_parts.append("净收益不足")

    if row["sharpe"] >= 2.0:
        strength_parts.append("风险调整收益强")
    elif row["sharpe"] < 1.0:
        weakness_parts.append("Sharpe 偏低")

    if row["max_drawdown"] > -0.05:
        strength_parts.append("回撤控制好")
    elif row["max_drawdown"] < -0.10:
        weakness_parts.append("回撤压力大")

    if row["average_turnover"] > 1.0:
        weakness_parts.append("换手高且交易成本敏感")
    elif row["average_turnover"] < 0.7:
        strength_parts.append("换手相对可控")

    if strategy == "spo_plus_fee":
        weakness_parts.append("当前输出与基础 SPO+ 几乎重合，费用项没有形成有效约束")
    if strategy == "spo_plus_turnover_l2":
        strength_parts.append("正则化显著改善基础 SPO+ 的净值表现")
    if strategy.startswith("ai_"):
        strength_parts.append("风险约束让波动和回撤更平滑")

    strength = "；".join(dict.fromkeys(strength_parts)) or "没有明显优势"
    weakness = "；".join(dict.fromkeys(weakness_parts)) or "主要风险来自样本期较短"
    return strength, weakness


def build_model_assessment(metrics: pd.DataFrame, ranking: pd.DataFrame) -> pd.DataFrame:
    net_models = metrics[
        (metrics["return_type"] == "net") & (metrics["strategy_group"] != "benchmark")
    ].copy()
    rank_lookup = ranking.set_index("strategy")["rank"].to_dict()
    rows = []
    for _, row in net_models.sort_values("annual_return", ascending=False).iterrows():
        strength, weakness = strategy_assessment(row)
        rows.append(
            {
                "rank": int(rank_lookup[row["strategy"]]),
                "strategy": row["strategy"],
                "strategy_group": row["strategy_group"],
                "strengths": strength,
                "weaknesses": weakness,
                "recommended_use": recommended_use(row),
            }
        )
    return pd.DataFrame(rows).sort_values("rank").reset_index(drop=True)


def recommended_use(row: pd.Series) -> str:
    strategy = row["strategy"]
    if strategy == "ai_low_risk":
        return "适合作为低波动核心组合或防守档位。"
    if strategy == "ai_medium_risk":
        return "适合作为当前样本下的主组合候选，收益和风险比较均衡。"
    if strategy == "ai_high_risk":
        return "适合愿意承受更高波动、追求更高收益弹性的配置。"
    if strategy == "spo_plus_turnover_l2":
        return "适合作为 SPO 类策略的生产化起点，但需要继续降低换手。"
    if strategy == "pto_markowitz":
        return "适合作为传统 PtO/Markowitz 对照模型，不宜忽略成本。"
    if strategy in {"spo_plus", "spo_plus_fee"}:
        return "更适合保留为消融实验基线，而非直接实盘化。"
    return "适合作为比较样本。"


def format_pct(value: float) -> str:
    return f"{value * 100:.2f}%"


def format_float(value: float) -> str:
    return f"{value:.2f}"


def markdown_table(frame: pd.DataFrame, columns: list[str]) -> str:
    if frame.empty:
        return "_No rows._"
    headers = "| " + " | ".join(columns) + " |"
    sep = "| " + " | ".join(["---"] * len(columns)) + " |"
    lines = [headers, sep]
    for _, row in frame[columns].iterrows():
        values = []
        for col in columns:
            value = row[col]
            if isinstance(value, (bool, np.bool_)):
                values.append("通过" if bool(value) else "未通过")
            elif isinstance(value, float):
                if any(token in col for token in ["return", "volatility", "drawdown", "rate", "turnover"]):
                    values.append(format_pct(value))
                elif "score" in col or "sharpe" in col or "ratio" in col or "calmar" in col:
                    values.append(format_float(value))
                else:
                    values.append(str(round(value, 4)))
            else:
                values.append(str(value))
        lines.append("| " + " | ".join(values) + " |")
    return "\n".join(lines)


